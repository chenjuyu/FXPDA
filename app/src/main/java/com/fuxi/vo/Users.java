package com.fuxi.vo;

/**
 * Title: Users Description: Users对象(存储用户基本信息)
 * 
 * @author LYJ
 * 
 */
public class Users {

    private String userid;
    private String code;
    private String name;

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
